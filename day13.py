def find_num_differences(list2, list1):
    difference =0
    length_diff = len(list2)-len(list1)
    smudge = -1
    for i in range(len(list1)):
        if list1[i] == (list2)[length_diff+i]:
            difference +=1
            smudge = i
            print(f"{i} can be changed for a different result {list1} {list2}")
    if difference == 1:
        return smudge
    else:
        return -1



